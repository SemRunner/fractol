#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*tmp_chr;

	tmp_chr = (unsigned char *)b;
	while (len--)
	{
		*tmp_chr = (unsigned char)c;
		tmp_chr++;
	}
	return (b);
}
