#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*s_chr;

	if (n == 0)
		return (NULL);
	s_chr = (unsigned char*)s;
	while (n--)
	{
		if (*s_chr == (unsigned char)c)
			return (void*)(s_chr);
		s_chr++;
	}
	return (NULL);
}
