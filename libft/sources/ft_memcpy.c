#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*dst_chr;
	unsigned char	*src_chr;

	if (n == 0 || (!dst && !src))
		return (dst);
	dst_chr = (unsigned char *)dst;
	src_chr = (unsigned char *)src;
	while (n--)
		*(dst_chr++) = *(src_chr++);
	return (dst);
}
